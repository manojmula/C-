#include<iostream>
#include<iomanip>
#include<cmath>

using namespace std;

int main()
{
    cout<<fixed<<setprecision(2);
    cout<<"sin(100) = "<<sin(100)<<endl;    //computing sine value
    cout<<"cos(100) = "<<cos(100)<<endl;    //computing cos value
    cout<<"tan(100) = "<<tan(100)<<endl;    //computing tan value
    cout<<"7 to the power of 6 = "<<pow(7,6)<<endl; //computing the power of 6 to 7
    cout<<"log10(10) = "<<log10(10)<<endl;
    cout<<"sqrt(2) = "<<sqrt(2)<<endl;

    return 0 ;
}

